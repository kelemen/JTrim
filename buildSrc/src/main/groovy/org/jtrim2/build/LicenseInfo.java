package org.jtrim2.build;

public final class LicenseInfo {
    private String name;
    private String url;

    public LicenseInfo() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }
}
